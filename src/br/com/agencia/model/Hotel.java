package br.com.agencia.model;

public class Hotel {
	private int hotel_id;
	private String hotel_rua;
	private String hotel_numero;
	private String hotel_bairro;
	private String hotel_cidade;
	private String hotel_rep;
	public int getHotel_id() {
		return hotel_id;
	}
	public void setHotel_id(int hotel_id) {
		this.hotel_id = hotel_id;
	}
	public String getHotel_rua() {
		return hotel_rua;
	}
	public void setHotel_rua(String hotel_rua) {
		this.hotel_rua = hotel_rua;
	}
	public String getHotel_numero() {
		return hotel_numero;
	}
	public void setHotel_numero(String hotel_numero) {
		this.hotel_numero = hotel_numero;
	}
	public String getHotel_bairro() {
		return hotel_bairro;
	}
	public void setHotel_bairro(String hotel_bairro) {
		this.hotel_bairro = hotel_bairro;
	}
	public String getHotel_cidade() {
		return hotel_cidade;
	}
	public void setHotel_cidade(String hotel_cidade) {
		this.hotel_cidade = hotel_cidade;
	}
	public String getHotel_rep() {
		return hotel_rep;
	}
	public void setHotel_rep(String hotel_rep) {
		this.hotel_rep = hotel_rep;
	}
	
	

}
